# Сложите все числа в списке, они могут быть отрицательными,
# если список пустой вернуть 0

list_1 = []
list_2 = [1, 2, 3]
list_3 = [1.1, 2.2, 3.3]
list_4 = [4, 5, 6]
list_5 = [i for i in range(101)]
# Суммирование всех чисел в списке через функцию sum()
print(sum(list_1))
print(sum(list_2))
print(sum(list_3))
print(sum(list_4))
print(sum(list_5))