my_list = list(map(str, input('–í–≤–µ–¥–∏—Ç–µ —Å–ø–∏—Å–æ–∫ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª: ').split(' ')))


def change_list(my_list):
    my_list_copy = list(my_list)
    new_elements = list(map(str, input('–í–≤–µ–¥–∏—Ç–µ —Å–ø–∏—Å–æ–∫ –Ω–æ–≤—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª: ').split(' ')))
    for i in range(len(new_elements)):
        my_list_copy.append(new_elements[i])
    return my_list_copy


print('–∏–∑–º–µ–Ω–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫: ', change_list(my_list))
