i = 1
while i < 101:  # Ограничение до 100
    if i % 3 == 0 and i % 5 == 0:  # если кратно 3 и 5 одновременно
        print("FuzzBuzz")
    else:
        if i % 5 == 0:  # если не кратно одновременно, но кратно 5
            print("Buzz")
        else:
            if i % 3 == 0:  # если не кратно одновременно, но кратно 3
                print("Fuzz")
            else:
                print(i)  # если не кратно ни 3, ни 5
    i += 1
