# программа на выявление поры года по номеру месяца

print('Введите месяц числом:')
month = int(input())

if month == 12 or month == 1 or month == 2:
    print('Это зима')
elif month > 2 and month < 6:
    print('Это весна')
elif month > 5 and month < 9:
    print('Это лето')
elif month > 8 and month < 12:
    print('Это осень')
else:
    print('Введены некорректные данные')
