# 1. Напишите программу по следующему описанию:
# двум переменным присваиваются числовые значения
print('Введите первое значение:')
a = int(input())
print('Введите второе значение:')
b = int(input())

if a > b:
    c = a - b  # если значение первой переменной больше второй, то найти разницу значений переменных (вычесть из первой вторую), результат связать с третьей переменной;
elif a < b:
    c = a + b  # если первая переменная имеет меньшее значение, чем вторая, то третью переменную связать с результатом суммы значений двух первых переменных;
else:
    c = a  # во всех остальных случаях, присвоить третьей переменной значение первой переменной
print('Значение третьей переменной', c)

